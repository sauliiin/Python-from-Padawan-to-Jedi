import math
num = float(input('Digite um número: '))
print('O número inteiro é {}'.format(math.trunc(num)))

from math import trunc
num = float(input('Digite um número: '))
print('O número inteiro é {}'.format(trunc(num)))

num = float(input('Digite um número: '))
print('O número inteiro é {}'.format(int(num)))
